print('Integer casting')
x = int(1)
y = int(2.8)
z = int("3")
print(x)
print(y)
print(z)

print('Float casting')
x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")
print(x)
print(y)
print(z)
print(w)

print('String casting')
x = str("s1")
y = str(2)
z = str(3.0)
print(x)
print(y)
print(z)

