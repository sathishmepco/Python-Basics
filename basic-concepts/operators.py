#Relational Operators
a = 22
b = 33
print( a > b )
print( a < b )
print( a <= b )
print( a >= b )
print( a == b )
print( a != b )

#Logical Operators
x = True
y = False
print( x and y )
print( x or y )
print( not x )

#special Operators
lang1 = 'Java'
lang2 = 'Python'
print( lang1 is not lang2 )
print( lang1 is lang2 )